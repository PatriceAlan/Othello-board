package strategies;

/**
 * L'énumération Difficulte représente les différents niveaux de difficulté possibles dans le jeu.
 * Elle peut être FACILE, MOYEN ou DIFFICILE.
 */
public enum Difficulte {
    // Représente le niveau de difficulté facile
    FACILE,
    // Représente le niveau de difficulté moyen
    MOYEN,
    // Représente le niveau de difficulté difficile
    DIFFICILE
}