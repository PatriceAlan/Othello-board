package composants_principaux;

/**
 * L'énumération Couleur représente les différentes couleurs possibles dans le jeu.
 * Elle peut être BLANC, NOIR ou VIDE.
 */
public enum Couleur {
    // Représente la couleur blanche
    BLANC,
    // Représente la couleur noire
    NOIR,
    // Représente une case vide
    VIDE
}